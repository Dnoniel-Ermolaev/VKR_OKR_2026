def expand_abbreviation(term: str) -> str:
    """Развернуть аббревиатуру в полное название (для тултипов и эпикриза)."""
    return ABBREVIATIONS.get(term, term)


def diagnosis_label(diagnosis_code: str) -> str:
    """Человеко-читаемая метка по коду диагноза."""
    return DIAGNOSIS_LABELS.get(diagnosis_code, diagnosis_code)


def diagnosis_color(diagnosis_code: str) -> str:
    """Цвет для UI-плашки по коду диагноза."""
    return DIAGNOSIS_COLOR.get(diagnosis_code, "#94a3b8")


